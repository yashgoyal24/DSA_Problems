class Solution{
    public:
    // Function to find number of bits needed to be flipped to convert A to B
    int countBitsFlip(int a, int b){
        
        // Your logic here
        
        //count no. of set bits in xor of both numbers
        
        int xor1=a^b;
        int counter=0;
        while(xor1>0){
            xor1=xor1&(xor1-1);
            counter++;
        }
        return counter;
    }
};

